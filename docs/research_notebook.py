sns.heatmap(cm_rf, annot=True, fmt='d', cmap='Blues',
            xticklabels=['No Burnout', 'Burnout'],
            yticklabels=['No Burnout', 'Burnout'])
plt.title('AI Random Forest\nConfusion Matrix')


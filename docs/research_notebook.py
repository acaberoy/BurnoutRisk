# Calculate learning curve for OPTIMIZED SVM (Cell 9 params)
train_sizes, train_scores, test_scores = learning_curve(
    SVC(**grid_search.best_params_, random_state=42, probability=True),
    X_train_smote, y_train_smote,
    cv=5,
    scoring='f1_macro',  # Balanced F1 for burnout classes
    train_sizes=np.linspace(0.1, 1.0, 10),  # 10 points from 10% to 100%
    n_jobs=-1
)

# Plotting
plt.figure(figsize=(10, 6))
plt.plot(train_sizes, np.mean(train_scores, axis=1), 'o-', color="r", linewidth=2, label="Training F1")
plt.plot(train_sizes, np.mean(test_scores, axis=1), 's-', color="g", linewidth=2, label="Cross-Validation F1")
plt.fill_between(train_sizes, np.mean(train_scores, axis=1) - np.std(train_scores, axis=1),
                 np.mean(train_scores, axis=1) + np.std(train_scores, axis=1), alpha=0.1, color="r")
plt.fill_between(train_sizes, np.mean(test_scores, axis=1) - np.std(test_scores, axis=1),
                 np.mean(test_scores, axis=1) + np.std(test_scores, axis=1), alpha=0.1, color="g")

plt.title("Work Productivity Dataset: SVM Learning Curve\n(30K Employees - Burnout Prediction)", fontsize=14)
